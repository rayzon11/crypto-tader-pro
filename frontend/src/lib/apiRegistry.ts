// API Key Storage
// ============================================================

const API_KEYS_STORAGE = "cryptobot_api_keys_registry";

export interface ApiKeyEntry {
  apiId: number;
  key: string;
  secret?: string;
  addedAt: string;
}

export function loadApiKeyRegistry(): ApiKeyEntry[] {
  if (typeof window === "undefined") return [];
  try {
    const raw = localStorage.getItem(API_KEYS_STORAGE);
    if (!raw) return [];
    return JSON.parse(raw) as ApiKeyEntry[];
  } catch {
    return [];
  }
}

export function saveApiKeyEntry(entry: ApiKeyEntry): void {
  if (typeof window === "undefined") return;
  const registry = loadApiKeyRegistry();
  const idx = registry.findIndex((e) => e.apiId === entry.apiId);
  if (idx >= 0) {
    registry[idx] = entry;
  } else {
    registry.push(entry);
  }
  localStorage.setItem(API_KEYS_STORAGE, JSON.stringify(registry));
}

export function removeApiKeyEntry(apiId: number): void {
  if (typeof window === "undefined") return;
  const registry = loadApiKeyRegistry().filter((e) => e.apiId !== apiId);
  localStorage.setItem(API_KEYS_STORAGE, JSON.stringify(registry));
}

export function isApiConnected(apiId: number): boolean {
  const registry = loadApiKeyRegistry();
  return registry.some((e) => e.apiId === apiId);
}
