export function getApisByCategory(cat: string): ApiEntry[] {
  return API_REGISTRY.filter((api) => api.category === cat);
}

export function getApisByPriority(priority: string): ApiEntry[] {
  return API_REGISTRY.filter((api) => api.priority === priority);
}

export function getConnectedApis(): ApiEntry[] {
  return API_REGISTRY.filter(
    (api) => api.status === "connected" || isApiConnected(api.id)
  );
}

export function getApisByAgent(agentName: string): ApiEntry[] {
  return API_REGISTRY.filter((api) => api.usedBy.includes(agentName));
}

export function getApiStats(): {
  total: number;
  connected: number;
  needsKey: number;
  premium: number;
  byCategory: Record<string, number>;
} {
  const connected = API_REGISTRY.filter(
    (api) => api.status === "connected" || isApiConnected(api.id)
  ).length;
  const needsKey = API_REGISTRY.filter(
    (api) => api.status === "needs_key" && !isApiConnected(api.id)
  ).length;
  const premium = API_REGISTRY.filter(
    (api) => api.status === "premium"
  ).length;

  const byCategory: Record<string, number> = {};
  for (const api of API_REGISTRY) {
    byCategory[api.category] = (byCategory[api.category] || 0) + 1;
  }

  return {
    total: API_REGISTRY.length,
    connected,
    needsKey,
    premium,
    byCategory,
  };
}

